# Switch Case

nilai = 95

match (nilai):
    
    case 95:
        print ("A")
        
    case 90:
        print ("B")
        
    case 85:
        print ("C")
        
    case 70:
        print ("D")
        
    case 60:
        print ("E")
        
    case _:
        print ("Semula")
     
        
        
        
# Switch Case 1

