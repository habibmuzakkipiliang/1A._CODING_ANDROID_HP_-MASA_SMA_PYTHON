b = int (input ("Masukkan angkanya B"))




# operator dasar

hasil = a + b
print ("Hasilnya adalah (tambah)", hasil)

hasil = a - b
print ("Hasilnya adalah (kurang)", hasil)

hasil = a * b
print ("Hasilnya adalah (kali)", hasil)

hasil = a / b
print ("Hasilnya adalah (bagi)", hasil)

hasil = a ** b
print ("Hasilnya adalah (pangkat)", hasil)

hasil = a / b
print ("Hasilnya adalah (bagi)", hasil)

hasil = a % b
print ("Hasilnya adalah (modulus)", hasil)




# perbandigan

hasil = a < b
print ("Apakah A lebih kecil dari B ?", hasil)

hasil = a > b
print ("Apakah A lebih besar dari B ?", hasil)

hasil = a == b
print ("Apakah A sama dengan B ?", hasil)

hasil = a != b
print ("Apakah A tidak sama dengan B ?", hasil)




# logika

hasil = (a < b) and (a == b)
print ("Apakah A lebih kecil dari B dan A sama dengan B ?", hasil)

hasil = (a < b) or (a == b)
print ("Apakah A lebih kecil dari B atau A sama dengan B ?", hasil)

hasil = not (a < b)
print ("Apakah bukan A lebih kecil dari B ?", hasil)

hasil = not (b)
print ("Apakah bukan b ?", hasil)

hasil = not (a)
print ("Apakah bukan a ?", hasil)